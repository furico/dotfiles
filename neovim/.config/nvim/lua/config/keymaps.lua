-- keymap 設定。プラグイン非依存のコア keymap のみを定義する。
-- nvim 0.12 のデフォルトと重複するもの（[d/]d, <C-w>d, ]b/[b, gcc 等）や、
-- options.lua の clipboard=unnamedplus と重複するクリップボード系は入れない。
-- leader 系はプラグイン導入回で名前空間ごと設計するため、ここでは扱わない。
-- desc は which-key 導入時にゼロコストで効くよう全マッピングに付与する。

local map = vim.keymap.set

-- ── 検索ハイライト ───────────────────────────────────────
-- 既定の <C-l>（再描画 + nohlsearch）をウィンドウ移動に使うため、
-- 検索ハイライト解除は <Esc> に割り当てる。
map("n", "<Esc>", "<cmd>nohlsearch<CR>", { desc = "検索ハイライトを解除" })

-- ── ウィンドウ移動 ───────────────────────────────────────
-- 既定に無い <C-hjkl> での移動を補う（標準は <C-w>h 等）。
map("n", "<C-h>", "<C-w>h", { desc = "左のウィンドウへ移動" })
map("n", "<C-j>", "<C-w>j", { desc = "下のウィンドウへ移動" })
map("n", "<C-k>", "<C-w>k", { desc = "上のウィンドウへ移動" })
map("n", "<C-l>", "<C-w>l", { desc = "右のウィンドウへ移動" })

-- ── ターミナル ───────────────────────────────────────────
map("t", "<Esc><Esc>", "<C-\\><C-n>", { desc = "ターミナルモードを抜ける" })

-- ── ビジュアル（インデント維持）──────────────────────────
-- インデント後も選択を保つ。select-mode を避けて x モードで定義する。
map("x", "<", "<gv", { desc = "インデント解除して選択を維持" })
map("x", ">", ">gv", { desc = "インデントして選択を維持" })

-- ── 検索ジャンプ ─────────────────────────────────────────
-- 次/前のマッチへ移動後、画面中央へ寄せ（zz）折りたたみを展開する（zv）。
map("n", "n", "nzzzv", { desc = "次のマッチへ（中央寄せ）" })
map("n", "N", "Nzzzv", { desc = "前のマッチへ（中央寄せ）" })
